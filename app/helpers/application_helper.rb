module ApplicationHelper

  def set_average_style(average)
    case average
    when "Deve Melhorar"
      return 'concept-dangerous'
    when "Pode Melhorar"
      return 'concept-bad'
    when "Regular"
      return 'concept-regular'
    when "Bom"
      return 'concept-good'
    when "Ótimo"
      return 'concept-excellent'
    else
      return nil
    end
  end

  def set_average_margin_top(average)
    if average == "Deve Melhorar" || average == "Pode Melhorar"
      return "margin-top-label-ranking"
    else
      return ""
    end
  end

  def checking_if_rating_is_visible(rating)
    if rating.rating_date != nil and rating.visible
      return true
    end
  end

  def returning_date(rating)
    return rating.rating_date.strftime("%d/%m/%Y")
  end

  def description_exists(rating)
    if rating.description != nil && !(rating.description.blank?)
      return true
    end
  end

  def flash_message
    messages = ""
    [:notice, :info, :warning, :error].each {|type|
      if flash[type]
        messages += "#{flash[type]}"
      end
    }

    messages
  end

